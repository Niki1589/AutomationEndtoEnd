package com.rms.automation.edm;

public class ProfileTemplate {

    private String analysisType;
    private String modelRegionCode;
    private String subRegion;
    private String perilName;
    private String softwareVersion;
    private String vendor;
    private String insuranceType;
    private String analysisMode;

    public String getAnalysisType() {
        return analysisType;
    }

    public void setAnalysisType(String analysisType) {
        this.analysisType = analysisType;
    }

    public String getModelRegionCode() {
        return modelRegionCode;
    }

    public void setModelRegionCode(String modelRegionCode) {
        this.modelRegionCode = modelRegionCode;
    }

    public String getSubRegion() {
        return subRegion;
    }

    public void setSubRegion(String subRegion) {
        this.subRegion = subRegion;
    }

    public String getPerilName() {
        return perilName;
    }

    public void setPerilName(String perilName) {
        this.perilName = perilName;
    }

    public String getSoftwareVersion() {
        return softwareVersion;
    }

    public void setSoftwareVersion(String softwareVersion) {
        this.softwareVersion = softwareVersion;
    }

    public String getVendor() {
        return vendor;
    }

    public void setVendor(String vendor) {
        this.vendor = vendor;
    }

    public String getInsuranceType() {
        return insuranceType;
    }

    public void setInsuranceType(String insuranceType) {
        this.insuranceType = insuranceType;
    }

    public String getAnalysisMode() {
        return analysisMode;
    }

    public void setAnalysisMode(String analysisMode) {
        this.analysisMode = analysisMode;
    }
}
